export const PixelEmoji: React.FC<PixelEmojiProps> = ({ moodId, color, size = 32 }) => {
  const grid = EMOJI_GRIDS[moodId] || EMOJI_GRIDS.joyeux;
  const cellSize = size / 8;

  return (
    <Svg width={size} height={size} viewBox={`0 0 ${size} ${size}`}>
      {grid.map((row, rIdx) =>
        row.map((val, cIdx) => {
          if (val === 1) {
            return (
              <Rect
                key={`${rIdx}-${cIdx}`}
                x={cIdx * cellSize}
                y={rIdx * cellSize}
                width={cellSize}
                height={cellSize}
                fill={color}
              />
            );
          }
          return null;
        })
      )}
    </Svg>
  );
};

export default PixelEmoji;
