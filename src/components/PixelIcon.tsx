export const PixelIcon: React.FC<PixelIconProps> = ({ type, color, size = 16 }) => {
  const grid = ICON_GRIDS[type] || ICON_GRIDS.play;
  const cellSize = size / 8;

  return (
    <Svg width={size} height={size} viewBox={`0 0 ${size} ${size}`}>
      {grid.map((row, rIdx) =>
        row.map((val, cIdx) => {
          if (val === 1) {
            return (
              <Rect
                key={`${rIdx}-${cIdx}`}
                x={cIdx * cellSize}
                y={rIdx * cellSize}
                width={cellSize}
                height={cellSize}
                fill={color}
              />
            );
          }
          return null;
        })
      )}
    </Svg>
  );
};

export default PixelIcon;
